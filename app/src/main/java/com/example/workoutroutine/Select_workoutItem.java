package com.example.workoutroutine;

public class Select_workoutItem {

    String name;
    int image;

    public Select_workoutItem(String name, int image){
        super();
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
